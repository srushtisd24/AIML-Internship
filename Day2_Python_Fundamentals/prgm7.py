# total bill
total_bill = float(input("Enter the total bill amount: "))

# number of people
people = int(input("Enter the number of people: "))

# calculating share person
share_per_person = total_bill / people

# Results
print("Total Bill:", total_bill)
print("Each person pays:", share_per_person)

# checking the data type of variables
print("Type of total_bill:", type(total_bill))
print("Type of people:", type(people))
print("Type of share_per_person:", type(share_per_person))
